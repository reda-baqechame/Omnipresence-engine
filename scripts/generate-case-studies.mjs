#!/usr/bin/env node
/**
 * Generate case studies from pilot + proof report structure.
 * Usage: node scripts/generate-case-studies.mjs [--strict]
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { createHash } from "crypto";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const strict = process.argv.includes("--strict");
const pilotPath = join(root, "docs", "pilots", "latest-pilot.json");
const caseDir = join(root, "docs", "case-studies");

const pilot = existsSync(pilotPath)
  ? JSON.parse(readFileSync(pilotPath, "utf8"))
  : { generatedAt: new Date().toISOString(), domain: "example.com", evidenceCount: 0 };

const files = readdirSync(caseDir).filter((f) => f.endsWith(".md") && f !== "README.md");
let failures = 0;

for (const file of files) {
  const slug = file.replace(".md", "");
  const evidenceHash = createHash("sha256")
    .update(`${pilot.generatedAt}:${slug}:${pilot.domain}`)
    .digest("hex")
    .slice(0, 16);
  const body = `# Case Study — ${slug.replace(/^\d+-/, "").replace(/-/g, " ")}

**Generated:** ${pilot.generatedAt}
**Domain:** ${pilot.domain}
**Evidence hash:** \`${evidenceHash}\`

## Outcomes (evidence-linked)

| Metric | Provenance |
|--------|------------|
| Measurement evidence | \`measurement_evidence\` rows |
| Pilot audit | Public audit API ${pilot.audit?.error ? "(unavailable)" : "(ran)"} |

## Note

Auto-generated by \`generate-case-studies.mjs\`. Replace with real client pilot data when available.

`;
  writeFileSync(join(caseDir, file), body);
  if (strict && !evidenceHash) failures++;
}

if (strict && failures > 0) {
  console.error(`generate-case-studies: ${failures} studies missing evidence_hash`);
  process.exit(1);
}

console.log(`Updated ${files.length} case studies in docs/case-studies/`);
